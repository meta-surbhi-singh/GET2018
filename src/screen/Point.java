package screen;

public class Point {
	private double x_coordinate;
	private double y_coordinate;
	
	public Point(double x, double y) {
		this.x_coordinate=x;
		this.y_coordinate=y;
	}
	
	public double getX() {
		return x_coordinate;
	}
	
	public double getY() {
		return y_coordinate;
	}

}
